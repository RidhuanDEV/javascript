// Javascript operators are used to perform operations on variables and values. and perform mathematical and logical computations.

// = is the assignment operator, used to assign values to variables.
// + is the add operator, used to add values to variables.
// * is multiplication operator, used to multiple values to variables.
// > is Comparison operator, used to compare values of variables.
// < is Comparison operator, used to compare values of variables.
// - is Subtraction operator, used to subtract values from variables.
// / is Division operator, used to divide values of variables.
// % is Modulus operator, used to find the remainder of division of two numbers.
// ** is Exponentiation operator, used to raise a number to the power of another number.
// && is Logical AND operator, used to combine two boolean expressions.
// || is Logical OR operator, used to combine two boolean expressions.
// ! is Logical NOT operator, used to negate a boolean expression.


// example

let x = 1; // Assignment operator
let y = 2; // Assignment operator

let sum = x + y; // add operator

let multiple = x * y; // multiplication operator

let isGreater = x < y; // Comparison operator

let subtraction = y - x; // Subtraction operator

let division = y / x; // division operator

let modulus = y % x; // modulus operator

let exponentiation = x ** y; // Exponentiation operator

let logicalAnd = (x < y) && (y > 0); // Logical AND operator

let logicalOr = (x < y) || (y < 0); // Logical OR operator

let logicalNot = !(x < y); // Logical NOT operator

x++; // Increment operator (postfix)
y--; // Decrement operator (postfix)

// Output the results

console.log("Sum:", sum); // Output: Sum: 3
console.log("Multiple:", multiple); // Output: Multiple: 2
console.log("Is x less than y?", isGreater); // Output: Is x less than y? true
console.log("Subtraction:", subtraction); // Output: Subtraction: 1
console.log("Division:", division); // Output: Division: 2
console.log("Modulus:", modulus); // Output: Modulus: 0
console.log("Exponentiation:", exponentiation); // Output: Exponentiation: 1
console.log("Logical AND:", logicalAnd); // Output: Logical AND: true
console.log("Logical OR:", logicalOr); // Output: Logical OR: true
console.log("Logical NOT:", logicalNot); // Output: Logical NOT: false

